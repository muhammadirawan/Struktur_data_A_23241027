# Perbandingan >, <, >=, <=, ==, !=, is, is not

x = 3
y = 4

# Lebih besar >
hasil = y > x
print (x, '>', y, '=', hasil)

# Lebih besar >
hasil = y < x
print (x, '<', y, '=', hasil)

# Lebih kecil sama dengan <=
hasil = y <= x
print (x, '<=', y, '=', hasil)
print (x, '<=', 3, '=', hasil)

# Lebih besar sama dengan >=
hasil = y >= x
print (x, '>=', y, '=', hasil)
print (x, '>=', 3, '=', hasil)

# == (sama dengan)
hasil = x == 9
print (x, '==', 9, '=', hasil)

# != (tidak sama dengan)
hasil = x != 9
print (x, '!=', 9, '=', hasil)

# is (adalah)
hasil = x is 9
print(x, 'is', 9, '=', hasil)

# is not (bukan)
hasil = x is not 9
print(x, 'is not', 9, '=', hasil)


